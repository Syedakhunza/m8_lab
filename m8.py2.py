def check_sum():
    
    num1 = int(input("Enter the first number: "))
    num2 = int(input("Enter the second number: "))

    
    total = num1 + num2

    
    if total > 10:
        print("The sum is greater than 10.")
    elif total < 10:
        print("The sum is less than 10.")
    else:
        print("The sum is equal to 10.")

check_sum()
